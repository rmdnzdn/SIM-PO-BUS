    public void autokode(){
        try {
            String tampilkode = "";
            Date date = new Date();
            SimpleDateFormat format = new SimpleDateFormat("YMM");
            String tgl = format.format(date);
            st = cn.createStatement();
            rs = st.executeQuery("SELECT MAX(RIGHT(kd_sopir,3)) FROM tbl_sopir WHERE kd_sopir LIKE '%"+tgl+"%' order by kd_sopir DESC limit 0,1");
            if(rs.next()){
                int no = rs.getInt(1)+1;
                if (no < 10) {
                    tampilkode = "SPR"+tgl+"00"+String.valueOf(no);
                } else if(no > 9 && no <= 99) {
                     tampilkode = "SPR"+tgl+"0"+String.valueOf(no);
                }else{
                    tampilkode = "SPR"+tgl+String.valueOf(no);
                }
                tkode.setText(tampilkode);
            }else{
                tkode.setText("SPR"+tgl+"001");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
