    public void tampildata(){
        try {
            int no = 1;
            st = cn.createStatement();
            tabmodel.getDataVector().removeAllElements();
            tabmodel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM tbl_sopir");
            while (rs.next()) {
               int b = no++;
                    Object[] data = {
                        b,
                        rs.getString("kd_sopir"),
                        rs.getString("nama"),
                        rs.getString("alamat"),
                        rs.getString("no_hp"),
                        rs.getString("no_sim"),
                        rs.getString("username"),
                        rs.getString("password"),
                        rs.getString("status_sopir")
                    };
                    tabmodel.addRow(data);
                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
